    public String getMailAdmin() {
        return mailAdmin;
    }

    public void setMailAdmin(String mailAdmin) {
        this.mailAdmin = mailAdmin;
    }

    public String getCodCaracteres() {
        return codCaracteres;
    }

    public void setCodCaracteres(String codCaracteres) {
        this.codCaracteres = codCaracteres;
    }

    public String getDirectorioWeb() {
        return directorioWeb;
    }

    public void setDirectorioWeb(String directorioWeb) {
        this.directorioWeb = directorioWeb;
    }

    public String getCache() {
        return cache;
    }

    public void setCache(String cache) {
        this.cache = cache;
    }
    
    
    
    
